class GroovyLogicalOperators {
    static void main(String[] args) {
        boolean x=true;
        boolean y=false;
        boolean z=true;
        //and operator
        println(x&&y);
        println(z&&x);
        //or operator
        println(x||y);
        println(z||x);
        //not operator
        println(!x);

    }
}
