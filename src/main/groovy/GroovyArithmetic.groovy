class GroovyArithmetic {
    static void main(String[] args) {
        int num1 =6;
        int num2=3;
        //sub
        println(num1-num2);
        //add
        println(num1+num2);
        //multiply
        println(num1*num2);
        //division
        println(num1/num2);
        //module
        println(num1%num2);
        //increment
        println(num1++);
        //decrement
        println(num1--);
    }
}
