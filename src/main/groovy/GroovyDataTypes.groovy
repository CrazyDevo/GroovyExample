class GroovyDataTypes {
    static void main(String[] args) {
        // we have same data types
        int x = 5;
        long y = 100L;
        float z = 10.5f;
        double b = 10.5e40;
        BigInteger bi = 30g;
        BigDecimal bd = 3.5g;

        String name = 'Doe';
        //case sensetive
        int a = 5;
        int A = 10;

        //we can use def with String
        def _name = 'Joe';

        //we can use def with int
        def _int = 9;

        //we can use def with ArrayList
        def array = [1, 2, 3, 4, 5, 6];





    }
}
